const lineC = rgb(0.7, 0.7, 0.7)
const accent = rgb(0.35, 0.2, 0.54)

let page = pdfDoc.addPage([PW, PH])
let y = PH - 50

function np(need = 55) {
  if (y < need) {
    page = pdfDoc.addPage([PW, PH])
    y = PH - 50
  }
}

function txt(t, x, yy, size, f = font, color = black) {
  page.drawText(t, { x, y: yy, size, font: f, color })
}

function txtC(t, size, f = fontB, color = black) {
  const w = f.widthOfTextAtSize(t, size)
  txt(t, L + (CW - w) / 2, y, size, f, color)
}

function hLine(yy, thickness = 0.5) {
  page.drawLine({ start: { x: L, y: yy }, end: { x: R, y: yy }, thickness, color: lineC })
}

function wrap(text, x, maxW, size, f = font, color = black, lh = 12) {
  const words = text.split(" ")
  let line = ""
  for (const word of words) {
    const test = line ? `${line} ${word}` : word
    if (f.widthOfTextAtSize(test, size) > maxW && line) {
      np(40)
      txt(line, x, y, size, f, color)
      y -= lh
      line = word
    } else {
      line = test
    }
  }
  if (line) {
    np(40)
    txt(line, x, y, size, f, color)
    y -= lh
  }
}

